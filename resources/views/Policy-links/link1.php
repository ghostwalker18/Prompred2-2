                        <p>Обсуждению данной темы была посвящена Конференция ООН на уровне министров, организованной четырьмя учреждениями ООН (Международной организацией труда (МОТ), Детским фондом ООН (ЮНИСЕФ), Продовольственной и сельскохозяйственной организацией ООН (ФАО) и Программой развития ООН (ПРООН)) 7 - 8 декабря в г. Алматы при поддержке Правительства Казахстана.</p>
